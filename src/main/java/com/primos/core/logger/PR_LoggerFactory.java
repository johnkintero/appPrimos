package com.primos.core.logger;
import org.apache.log4j.Logger;
import org.apache.log4j.spi.LoggerFactory;

/**
 * This class create a new factory of PR_Logger, create a new instance of Log4j whit basic configuration.
 * This log work whit the pattern define in com.ps.commons.constants.PR_CommonStrConstants.PATTERN_EXIT_LOG, 
 * the configuration for exit file in com.ps.commons.constants.PR_CommonStrConstants.PAHT_EXIT_LOG_LOG4J and
 * level ERROR by default.
 * @version 1.0
 * @since 14/10/2018
 * @author GAOQ
 */
public class PR_LoggerFactory implements LoggerFactory {

    /**
     * Create a new instance of this logger.
     * @param name String name of a class that this logger it belogns.
     * @return Logger the new instance of Logger.
     */
    @Override
    public Logger makeNewLoggerInstance(String name) {
        return new PR_Logger(name);
    }
}